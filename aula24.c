#include <stdio.h>
#include <stdlib.h>

/*
    Aula 24 - Operadores matemáticos

    Soma -> +
    Subtração -> -
    Multiplicação -> *
    Divisão -> /
*/  

int main()
{   
    int x = 10, y = 20;

    printf("Soma: %d\n", x + y);
    printf("Subtração: %d\n", x - y);
    printf("Multiplicação: %d\n", x * y);
    printf("Divisão: %d\n", x / y);

    return 0;
}